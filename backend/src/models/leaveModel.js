import mongoose from 'mongoose'

const leaveSchema = new mongoose.Schema({
    employee:{
        type: mongoose.Schema.Types.ObjectId,
        ref: "User",
        required: true
    },
    leaveType:{
        type: String,
        enum:["casual", "sick", "paid"],
        required: true
    },
    startDate:{
        type: Date,
        required: true
    },
    endDate:{
        type: Date,
        required: true
    },
    reason:{
        type: String,
        required: true
    },
    status:{
        type: String,
        enum:["pending", "approved", "rejected"],
        default: "pending"
    },
    appliedAt:{
        type: Date,
        default: Date.now
    }
}, {timestamps: true})


export const Leave = mongoose.model("Leave", leaveSchema)